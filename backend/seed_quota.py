def main():
    db = SessionLocal()
    try:
        # Clear old data
        db.query(LineItemQuotaBinding).delete()
        db.query(CalcResult).delete()
        db.execute(BoqItem.__table__.update().values(is_dirty=1))

        # Clear existing quotas and re-seed
        db.query(QuotaItem).delete()
        db.commit()

        count = 0
        for code, name, unit, labor, material, machine in QUOTA_DATA:
            item = QuotaItem(
                quota_code=code,
                name=name,
                unit=unit,
                labor_qty=labor,
                material_qty=material,
                machine_qty=machine,
            )
            db.add(item)
            count += 1

        db.commit()
        print(f"✅ 已导入 {count} 条定额数据")
        print(f"✅ 已清除所有绑定和计算结果")

        # Verify
        total = db.query(QuotaItem).count()
        print(f"✅ 定额库总计: {total} 条")

    finally:
        db.close()


if __name__ == "__main__":
    main()
